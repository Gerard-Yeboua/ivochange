    <script>
        document.addEventListener('DOMContentLoaded', () => {
            const amountInput = document.getElementById('amount');
            const feesInput = document.getElementById('fees');
            const convertedAmountInput = document.getElementById('convertedAmount');
            const fromCurrencySelect = document.getElementById('fromCurrency');
            const toCurrencySelect = document.getElementById('toCurrency');

            function calculateFeesAndConversion() {
                const amount = parseFloat(amountInput.value);
                const fromCurrency = fromCurrencySelect.value;
                const toCurrency = toCurrencySelect.value;
                const feeRate = 0.01; // 1% de frais

                if (!isNaN(amount)) {
                    const amountWithFees = amount * (1 + feeRate);
                    feesInput.value = amountWithFees.toFixed(2); // Fixer à 2 décimales

                    // Appeler une API ou effectuer un calcul de conversion ici
                    fetch(`convert.php?amount=${amountWithFees}&from=${fromCurrency}&to=${toCurrency}`)
                        .then(response => response.json())
                        .then(data => {
                            if (data.success) {
                                convertedAmountInput.value = data.convertedAmount.toFixed(2);
                            } else {
                                convertedAmountInput.value = 'Erreur';
                            }
                        })
                        .catch(error => {
                            console.error('Erreur:', error);
                            convertedAmountInput.value = 'Erreur';
                        });
                } else {
                    feesInput.value = '';
                    convertedAmountInput.value = '';
                }
            }

            amountInput.addEventListener('input', calculateFeesAndConversion);
            fromCurrencySelect.addEventListener('change', calculateFeesAndConversion);
            toCurrencySelect.addEventListener('change', calculateFeesAndConversion);
        });
